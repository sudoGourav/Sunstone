public class forloop {
    public static void main(String[] args) {
        int i = 1;
        for(i = 1; i<= 10; i++){
            System.out.println(i);
        }

        int j = 1;
        for(;j<=10;){
            System.out.println(j);
            j++;
        }
        for(;j<=5; j++){
            System.out.println(j);

        }

    }
}
